# Python Data Types: Overview and Examples

# 1. Numeric Types: int, float, complex
integer = 10
floating_point = 20.5
complex_number = 3 + 4j

print("Integer:", integer)
print("Float:", floating_point)
print("Complex:", complex_number)

# 2. Text Type: str
string_example = "Hello, Python!"
print("String:", string_example)

# 3. Boolean Type: bool
is_true = True
is_false = False
print("Boolean True:", is_true, "| Boolean False:", is_false)

# 4. Sequence Types: list, tuple, range
list_example = [1, 2, 3]
tuple_example = (1, 2, 3)
range_example = range(5)

print("List:", list_example)
print("Tuple:", tuple_example)
print("Range:", list(range_example))

# 5. Mapping Type: dict
dictionary_example = {"name": "Alice", "age": 25}
print("Dictionary:", dictionary_example)

# 6. Set Types: set, frozenset
set_example = {1, 2, 3}
frozenset_example = frozenset({1, 2, 3})

print("Set:", set_example)
print("Frozenset:", frozenset_example)

# 7. None Type
none_example = None
print("None Type:", none_example)